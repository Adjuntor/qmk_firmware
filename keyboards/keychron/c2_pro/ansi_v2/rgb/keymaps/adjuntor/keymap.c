#include QMK_KEYBOARD_H

enum layers{
  MAC_BASE,
  MAC_FN,
  WIN_BASE,
  WIN_FN,
  WIN_NIKKE,
  WIN_MAPLE,
  WIN_FORTNITE,
};


enum custom_keycodes {
    STAB = SAFE_RANGE,
    ASSASSINATE,
    ALICEMACRO,
    JAM,
    LMOUSECLICK,
};

bool spam_assassinate;
bool spam_stab;
bool spam_alicemacro;
bool spam_jam = false;
bool spam_lmouseclick = false;
uint32_t spam_timer = 0;


int randy(int max, int min){
   return (rand() % (max + 1 - min)) + min;
}

#define KC_TASK LGUI(KC_TAB)
#define KC_FLXP LGUI(KC_E)

bool win_mode;   // declare global variable

bool dip_switch_update_user(uint8_t index, bool active) { 
    if(index == 0 && active) { 
        win_mode = true;
    } else {
        win_mode = false;
    }
    return true;
}

bool process_record_user(uint16_t keycode, keyrecord_t* record) {
    switch (keycode) {
    case ASSASSINATE:
        if (record->event.pressed) {
            spam_assassinate = true;
        } else {
            spam_assassinate = false;
        }
        break;

    case STAB:
        if (record->event.pressed) {
            spam_stab = true;
        } else {
            spam_stab = false;
        }
        break;

    case ALICEMACRO:
        if (record->event.pressed) {
            spam_alicemacro = true;
        } else {
            spam_alicemacro = false;
        }
        break;

    case JAM:
        if (record->event.pressed) {
            spam_jam = !spam_jam;
        } else {
            //nothing
        }
        break;
    
    case LMOUSECLICK:
        if (record->event.pressed) {
            spam_lmouseclick = !spam_lmouseclick;
        } else {
            //nothing
        }
        break;
    }
    return true;
};


void matrix_scan_user(void) {
  if (spam_assassinate) {
    SEND_STRING( SS_TAP(X_LEFT_CTRL) SS_TAP(X_Y));
  }
  if (spam_stab) {
    SEND_STRING( SS_TAP(X_LEFT_SHIFT) SS_TAP(X_Y) SS_TAP(X_U));
  }
  if (spam_alicemacro) {
    if (timer_elapsed32(spam_timer) > randy(50,20)) {
        tap_code_delay(KC_MS_BTN1, randy(300,200));
        spam_timer = timer_read32();
    }
  }
  if (spam_jam) {
    if (timer_elapsed32(spam_timer) > randy(150000,120000)) {
        SEND_STRING(SS_DOWN(X_B) SS_DELAY(200) SS_TAP(X_SPACE) SS_DELAY(200) SS_UP(X_B));
        spam_timer = timer_read32();
    }
  }
  if (spam_lmouseclick) {
    if (timer_elapsed32(spam_timer) > randy(100,80)) {
        tap_code_delay(KC_MS_BTN1, randy(50,20));
        spam_timer = timer_read32();
    }
  }

};

bool rgb_matrix_indicators_user(void) {
    uint8_t current_layer = get_highest_layer(layer_state);
    switch (current_layer) {
        case MAC_BASE:
        case WIN_BASE:
            if (win_mode) {
                rgb_matrix_set_color_all(0x2C, 0x52, 0x8C);  // RGB YInMn Blue
            } else {
                rgb_matrix_set_color_all(0x9E, 0x9E, 0x9E);  // RGB white
            }
            break;
        case WIN_FN:
            rgb_matrix_set_color_all(0x00, 0x9E, 0x00);  // RGB green
            break;
        case MAC_FN:
            rgb_matrix_set_color_all(0x51, 0xA0, 0xD5);  // RGB Carolina Blue
            break;
        case WIN_NIKKE:
            rgb_matrix_set_color_all(0x9E, 0x00, 0x00);  // RGB red
            break;
        case WIN_MAPLE:
            rgb_matrix_set_color_all(0xFF, 0x22, 0x00);  // RGB orange
            break;
        case WIN_FORTNITE:
            rgb_matrix_set_color_all(0x5F, 0xCE, 0xEA);  // RGB sky blue
            break;
        default:
            break;
    }
    return false;
}



